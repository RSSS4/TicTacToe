package tictactoe.model;

public class UltrScore {
    private int raw;
    private int col;
    private int score;

    public int getRaw() {
        return raw;
    }

    public int getCol() {
        return col;
    }

    public int getScore() {
        return score;
    }

    public void setRaw(int raw) {
        this.raw = raw;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
